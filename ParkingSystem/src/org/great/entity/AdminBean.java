package org.great.entity;

public class AdminBean {
	private String adminId; //后台用户id
	private String adminAccount;//账户名
	private String adminPwd;//密码
	private String adminName;//姓名
	private String adminSex;//性别
	private String adminTel;//电话
	private String adminAddr;//住址
	private String adminIDCard;//身份证
	private String adminState;//后台用户状态
	private String adminRegTime;//注册时间
	public AdminBean() {
		
	}
	
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAdminAccount() {
		return adminAccount;
	}
	public void setAdminAccount(String adminAccount) {
		this.adminAccount = adminAccount;
	}
	public String getAdminPwd() {
		return adminPwd;
	}
	public void setAdminPwd(String adminPwd) {
		this.adminPwd = adminPwd;
	}
	public String getAdminName() {
		return adminName;
	}
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}
	public String getAdminSex() {
		return adminSex;
	}
	public void setAdminSex(String adminSex) {
		this.adminSex = adminSex;
	}
	public String getAdminTel() {
		return adminTel;
	}
	public void setAdminTel(String adminTel) {
		this.adminTel = adminTel;
	}
	public String getAdminAddr() {
		return adminAddr;
	}
	public void setAdminAddr(String adminAddr) {
		this.adminAddr = adminAddr;
	}
	public String getAdminIDCard() {
		return adminIDCard;
	}
	public void setAdminIDCard(String adminIDCard) {
		this.adminIDCard = adminIDCard;
	}
	public String getAdminState() {
		return adminState;
	}
	public void setAdminState(String adminState) {
		this.adminState = adminState;
	}
	public String getAdminRegTime() {
		return adminRegTime;
	}
	public void setAdminRegTime(String adminRegTime) {
		this.adminRegTime = adminRegTime;
	}
	
	
}
